
public class Name {
	private String hebrew, english;
	public Name(String hebrew, String english)
	{
		this.hebrew = hebrew;
		this.english = english;
	}
	public String getHebrew() {
		return hebrew;
	}
	public void setHebrew(String hebrew) {
		this.hebrew = hebrew;
	}
	public String getEnglish() {
		return english;
	}
	public void setEnglish(String english) {
		this.english = english;
	}
	
	
	
}
